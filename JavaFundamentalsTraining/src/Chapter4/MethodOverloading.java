package Chapter4;

public class MethodOverloading {
    public static void main(String[] args) {

    }

    public static int calSum (int x, int y)
    {
        return x + y;
    }

    public static double calSum (double x, double y)
    {
        return x + y;
    }

    public static float calSum   (float x, float y)
    {
        return x + y;
    }


}
